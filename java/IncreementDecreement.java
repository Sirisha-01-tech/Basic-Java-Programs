public class IncreementDecreement {
    public static void main(String args[]){
        int a=10;
        int b=20;
        System.out.println("Post Increement of a is:"+(a++));
        System.out.println("Post Decreement of a is:"+(a--));
        System.out.println("Pre Increement of a is:"+(++a));
        System.out.println("Pre Decreement of a is:"+(--a));
        System.out.println("Post Increement of b is:"+(b++));
        System.out.println("Post Decreement of b is:"+(b--));
        System.out.println("Pre Increement of b is:"+(++b));
        System.out.println("Pre Decreement of b is:"+(--b));

    }
    
}
